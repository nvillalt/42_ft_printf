#include "libft.h"

void	ft_bzero(void *ptr, size_t n)
{
	size_t	i;
	char	*copy;

	i = 0;
	copy = (char *)ptr;
	while (i < n)
	{
		copy[i] = '\0';
		i++;
	}
}
